package mb;

public class Item {

private int shelfNr;
	
	private String name;
	
	private int price;
	
	private String sellJs;
	

	public Item(){
		
	}


	public int getShelfNr() {
		return shelfNr;
	}


	public void setShelfNr(int shelfNr) {
		this.shelfNr = shelfNr;
	}


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}


	public int getPrice() {
		return price;
	}


	public void setPrice(int price) {
		this.price = price;
	}


	public String getSellJs() {
		return sellJs;
	}


	public void setSellJs(String sellJs) {
		this.sellJs = sellJs;
	}
	
	
}

